    function show_gold_receive() {
        let money = parseInt($("#money").val());
        if(money >= 10000 && money % 1000 == 0) {
            let tile = 1.5;
			if(money >= 101000 && money <= 200000) {
				tile = 1.6;
			} else if(money >= 201000 && money <= 300000) {
				tile = 1.7;
			} else if(money >= 301000 && money <= 500000) {
				tile = 2;
			} else if(money >= 501000 && money <= 999000) {
				tile = 2.1;
			} else if(money >= 1000000) {
				tile = 2.3;
			}
            $("#gold_receive").val(Math.ceil(money*tile/1000));
            $("#err_receive").addClass("d-none");
        } else {
            $("#gold_receive").val(0);
            $("#err_receive").removeClass("d-none");
        }
    }

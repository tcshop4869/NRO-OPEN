    $(document).ready(function() {
        $('#transfer').click(function() {
            $('#transfer').html('Đang thực hiện...');
            $('#transfer').prop('disabled', true);

            let money = $.trim($('#money').val());
            let player_id = $.trim($('#player_id').val());

